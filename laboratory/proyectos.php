	          <hr>
	          <p>OWL Desarrollos utiliza la metodologia SCRUM para llevar a cabo el proyecto
	          de desarrollo solicitado, un proyecto tiene diferentes etapas, como electronica,
	          mecanica, programación, etc. En cada una de las etapas se sigue un procsedimiento
	          de seguimiento y colaboración en conjunto con el cliente.</p>
	        
	        <br>
	        
			<a href="#"><img src="img/phases1.png" height="618" width="800" alt=""></a>
	        
	        <br>
	        <br>
	        
	        <p> <strong> Pre proyecto: </strong> En esta etapa se ivestiga y enetiende los requerimeintos
	        del cliente y del proyecto, como resultado de esta estapa se obtiene un acta de proyecto 
	        con el cual sera la guia que permitira trazar los resultados en el desarrollo del proyecto.</p>
	        
	        <p> <strong> Suministro: </strong> En esta etapa se analizan los elementos necesarios para poder
	        obetener los suministros que requerira el proyecto, como resultado de esta etapa se obtiene
	        la lista de material para poder construir el prototipo asi como la estrategia de ensable o desarrollo
	        correspondiente, los puntos criticos en esta etapa son el lead-time de cada proveedor. </p>
	        
	        <p> <strong> Diseño: </strong> Esta etapa es una de las más criticas dentro de las fases del proyecto
	        debido a que en esta etapa el área de ingenieria o proyectos canaliza su recurso tecnico en las actividades
	        asignadas en el pre proyecto, las actividades pueden ir desde programación, diseño electrónico, diseño mecanico.</p>
	        
	        <p> <strong> Implementación: </strong> En esta etapa se empieza la operación del proyecto, con sub ensambles o 
	        ensambles planeados en la etapa 1, en el caso de la mécanica es en esta etapa donde se obtiene los elementos
	        mecanicos diseñados en la etapa previa, esta etapa tiene como resultado un primer prototipo fisico del proyecto o
	        un primer realese del software solicitado.</p>
	        
	        <p> <strong> Maduración: </strong> Posterior a la generación del prototipo, esté se lleva a campo o en laboratorio
	        para realizar pruebas de funcionamiento, rendimiento y el cumplimiento de las caracteristicas solicitadas en el 
	        pre proyecto.</p>
	        
	        <p> <strong> Actualización: </strong> Una ves probado el prototipo se actualiza la información del proyecto con las 
	        correcciones (en caso de haber) y/o mejoras obetenidas apartir de la retro alimentación del prototipo.</p>
	        
	        <p> <strong> Producción: </strong> En esta etapa el proyecto es lanzado a mercado con una producción en serie del mismo,
	        minimizando errores y posibles fallas.</p>
	        
